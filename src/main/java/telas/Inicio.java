    private void tableestoqueMouseClicked(java.awt.event.MouseEvent evt) {                                          
    int linha = tableestoque.getSelectedRow();
    
    // Preenche os campos de texto com os dados da linha selecionada
    // O índice (0, 1, 2...) deve seguir a ordem das suas colunas
    jTextField1.setText(tableestoque.getValueAt(linha, 1).toString()); // Nome
    jTextField2.setText(tableestoque.getValueAt(linha, 2).toString()); // Descrição
    jTextField3.setText(tableestoque.getValueAt(linha, 3).toString()); // Quantidade
    jTextField4.setText(tableestoque.getValueAt(linha, 4).toString()); // Preço
}
    
    
    
    
    
    
    
    public void preencherTabela() {
    EstoqueDAO dao = new EstoqueDAO();
    List<EstoqueBean> lista = dao.listarTudo();
    
   
    DefaultTableModel modelo = (DefaultTableModel) tableestoque.getModel();
    
    
    modelo.setNumRows(0); 

    
    for (EstoqueBean item : lista) {
        modelo.addRow(new Object[]{
            item.getId(),
            item.getNome(),
            item.getDescricao(),
            item.getQuantidade(),
            item.getPreco()
        });
    }
    
 } 
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                                                   
    EstoqueBean eb = new EstoqueBean();
    EstoqueDAO dao = new EstoqueDAO();

   
    eb.setNome(jTextField1.getText());
    eb.setDescricao(jTextField2.getText());
    eb.setQuantidade(Integer.parseInt(jTextField3.getText()));
    eb.setPreco(Double.parseDouble(jTextField4.getText()));

   
    dao.cadastrar(eb);

 
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    
    JOptionPane.showMessageDialog(null, "Item adicionado ao estoque!");
     preencherTabela(); //vai atualizar a tabela
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
                                                
    int linha = tableestoque.getSelectedRow(); // ISSO TEM Q PEGAR A LINHA CLICADA, PELO AMOR DE DEUS 
    
    if (linha != -1) {
        
        int id_linha_selecionada = (int) tableestoque.getValueAt(linha, 0);
        
        EstoqueDAO dao = new EstoqueDAO();
        dao.excluir(id_linha_selecionada); 
        
        preencherTabela(); //vai atualizar a tabela
        JOptionPane.showMessageDialog(null, "Item excluído com sucesso!");
    } else {
        JOptionPane.showMessageDialog(null, "Selecione um item na tabela para excluir.");
    }


    }//GEN-LAST:event_jButton2ActionPerformed

